#Faça um aloritmo que leia o salário de um funcionário e mostre seu novo salário, com 15% de aumento.
n = float(input('Parabéns!! Você ganhou um aumento de 15% no seu salário, quanto você ganha agora? '))
print('Se voce ganha {:.2f} reais agora, seu novo salário será de {:.2f} reais!!'.format(n, n*1.15))
